#!/usr/bin/env node
/*
 * Patches `detox.config.js`'s android.cloud app config with the BrowserStack
 * bs:// URLs exported by upload-apk-to-browserstack.sh. Writes to
 * detox.cloud.config.js so the original stays clean.
 */
const fs = require('fs');
const path = require('path');

const APP_URL = process.env.BS_APP_URL;
const TEST_URL = process.env.BS_TEST_URL;

if (!APP_URL || !TEST_URL) {
  console.error('BS_APP_URL and BS_TEST_URL must be set in the environment.');
  process.exit(1);
}

const base = require(path.resolve(__dirname, '..', 'detox.config.js'));

// Override android.cloud app config to use the bs:// URLs
base.apps['android.cloud'] = {
  ...base.apps['android.cloud'],
  binaryPath: APP_URL,
  testBinaryPath: TEST_URL
};

const out = `/* auto-generated by scripts/write-cloud-config.js */\nmodule.exports = ${JSON.stringify(base, null, 2)};\n`;
fs.writeFileSync(path.resolve(__dirname, '..', 'detox.config.cloud.js'), out);
console.log('Wrote detox.config.cloud.js with bs:// URLs.');
